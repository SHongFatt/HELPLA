package edu.singaporetech.helpla.models;

public class Post {
    String lostItemImg;
    String title;
    String desc;
    String lastSeenloc;

    public String getLostItemImg() {
        return lostItemImg;
    }

    public void setLostItemImg(String lostItemImg) {
        this.lostItemImg = lostItemImg;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getLastSeenloc() {
        return lastSeenloc;
    }

    public void setLastSeenloc(String lastSeenloc) {
        this.lastSeenloc = lastSeenloc;
    }



}
